test.describe('Unit: Domain Blocking/Whitelisting', () => {
  function isDomainBlocked(hostname, blockedDomains) {
    if (!blockedDomains || !Array.isArray(blockedDomains) || blockedDomains.length === 0) {
      return false;
    }
    return blockedDomains.some(blocked => 
      typeof blocked === 'string' && hostname.includes(blocked)
    );
  }

  test.it('returns false for empty blocklist', () => {
    test.assertFalse(isDomainBlocked('example.com', []), 'Empty list should not block');
  });

  test.it('returns false for null blocklist', () => {
    test.assertFalse(isDomainBlocked('example.com', null), 'Null should not block');
  });

  test.it('blocks exact domain match', () => {
    test.assertTrue(isDomainBlocked('evil.com', ['evil.com']), 'Exact match should block');
  });

  test.it('blocks subdomain match', () => {
    test.assertTrue(isDomainBlocked('sub.evil.com', ['evil.com']), 'Subdomain should block');
  });

  test.it('does not block non-matching domain', () => {
    test.assertFalse(isDomainBlocked('good.com', ['evil.com']), 'Non-match should not block');
  });

  test.it('filters non-string entries', () => {
    test.assertFalse(isDomainBlocked('example.com', [null, 123, undefined]), 'Non-strings should be filtered');
  });

  test.it('handles multiple blocked domains', () => {
    test.assertTrue(isDomainBlocked('bad.com', ['evil.com', 'bad.com', 'awful.com']), 'Should match second domain');
  });
});

