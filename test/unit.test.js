test.describe('Unit: Analytics Report Generation', () => {
  function generateAnalyticsReport(analytics) {
    const blockRate = analytics.analyzed > 0 
      ? ((analytics.blocked / analytics.analyzed) * 100).toFixed(2)
      : 0;
    
    return {
      totalAnalyzed: analytics.analyzed || 0,
      totalBlocked: analytics.blocked || 0,
      blockRate: `${blockRate}%`,
      sessionDuration: Date.now() - analytics.sessionStart,
      topDomains: Object.entries(analytics.domains || {})
        .filter(([domain, count]) => 
          typeof domain === 'string' && Number.isInteger(count) && count > 0
        )
        .sort(([, a], [, b]) => (b || 0) - (a || 0))
        .slice(0, 10)
        .map(([domain, count]) => ({ domain, count }))
    };
  }

  test.it('calculates 0% block rate for no analysis', () => {
    const report = generateAnalyticsReport({
      analyzed: 0,
      blocked: 0,
      sessionStart: Date.now(),
      domains: {}
    });
    test.assertEqual(report.blockRate, '0%', 'Should be 0% for no analysis');
  });

  test.it('calculates 50% block rate', () => {
    const report = generateAnalyticsReport({
      analyzed: 100,
      blocked: 50,
      sessionStart: Date.now(),
      domains: {}
    });
    test.assertEqual(report.blockRate, '50.00%', 'Should calculate correct percentage');
  });

  test.it('calculates 0% block rate with no blocks', () => {
    const report = generateAnalyticsReport({
      analyzed: 100,
      blocked: 0,
      sessionStart: Date.now(),
      domains: {}
    });
    test.assertEqual(report.blockRate, '0.00%', 'Should be 0% with no blocks');
  });

  test.it('filters invalid domains', () => {
    const report = generateAnalyticsReport({
      analyzed: 10,
      blocked: 5,
      sessionStart: Date.now(),
      domains: {
        'example.com': 5,
        'bad.com': 2
      }
    });
    test.assertTrue(report.topDomains.length <= 2, 'Should filter invalid entries');
  });

  test.it('limits to top 10 domains', () => {
    const domains = {};
    for (let i = 0; i < 20; i++) {
      domains[`domain${i}.com`] = 100 - i;
    }
    const report = generateAnalyticsReport({
      analyzed: 100,
      blocked: 50,
      sessionStart: Date.now(),
      domains
    });
    test.assertTrue(report.topDomains.length <= 10, 'Should limit to 10 domains');
  });

  test.it('includes session duration', () => {
    const report = generateAnalyticsReport({
      analyzed: 10,
      blocked: 5,
      sessionStart: Date.now() - 5000,
      domains: {}
    });
    test.assertTrue(report.sessionDuration >= 5000, 'Should calculate session duration');
  });
});

