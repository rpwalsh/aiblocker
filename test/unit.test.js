test.describe('Unit: calculateKeywordScore()', () => {
  // Mock function (from background.js)
  function calculateKeywordScore(content, terms) {
    try {
      let score = 0;
      const validTerms = Array.isArray(terms) ? terms.filter(t => typeof t === 'string') : [];
      
      validTerms.forEach(term => {
        if (content.includes(term.toLowerCase())) {
          score += 0.3;
        }
      });
      return Math.min(score, 1);
    } catch (e) {
      return 0;
    }
  }

  test.it('returns 0 for empty content', () => {
    const result = calculateKeywordScore('', ['ai-generated']);
    test.assertEqual(result, 0, 'Empty content should score 0');
  });

  test.it('returns 0 for no matched terms', () => {
    const result = calculateKeywordScore('This is normal content', ['ai-generated', 'midjourney']);
    test.assertEqual(result, 0, 'No matches should score 0');
  });

  test.it('returns 0.3 for single match', () => {
    const result = calculateKeywordScore('This was ai-generated', ['ai-generated']);
    test.assertEqual(result, 0.3, 'One match should score 0.3');
  });

  test.it('returns 0.6 for two matches', () => {
    const result = calculateKeywordScore('ai-generated by midjourney', ['ai-generated', 'midjourney']);
    test.assertEqual(result, 0.6, 'Two matches should score 0.6');
  });

  test.it('caps score at 1.0', () => {
    const result = calculateKeywordScore('ai-generated midjourney dalle stable diffusion', 
      ['ai-generated', 'midjourney', 'dalle', 'stable diffusion']);
    test.assertEqual(result, 1, 'Score should cap at 1.0');
  });

  test.it('is case-insensitive', () => {
    const result = calculateKeywordScore('ai-generated', ['AI-GENERATED']);
    test.assertEqual(result, 0.3, 'Should be case-insensitive');
  });

  test.it('handles invalid terms array', () => {
    const result = calculateKeywordScore('ai-generated', null);
    test.assertEqual(result, 0, 'Should handle null terms');
  });

  test.it('filters non-string terms', () => {
    const result = calculateKeywordScore('ai-generated', ['ai-generated', 123, null]);
    test.assertEqual(result, 0.3, 'Should filter non-string terms');
  });
});

