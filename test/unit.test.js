test.describe('Unit: Trial Period Calculation', () => {
  function calculateTrialDaysRemaining(trialStart) {
    try {
      if (!trialStart || typeof trialStart !== 'number') {
        return 30;
      }
      const elapsed = Date.now() - trialStart;
      const daysElapsed = Math.floor(elapsed / (24 * 60 * 60 * 1000));
      const daysRemaining = Math.max(0, 30 - daysElapsed);
      return daysRemaining;
    } catch (e) {
      return 30;
    }
  }

  test.it('returns 30 for null start date', () => {
    const result = calculateTrialDaysRemaining(null);
    test.assertEqual(result, 30, 'Null should default to 30 days');
  });

  test.it('returns 30 for just installed', () => {
    const result = calculateTrialDaysRemaining(Date.now());
    test.assertEqual(result, 30, 'Just installed should be 30 days');
  });

  test.it('returns ~29 for 1 day old', () => {
    const oneDayAgo = Date.now() - (1 * 24 * 60 * 60 * 1000);
    const result = calculateTrialDaysRemaining(oneDayAgo);
    test.assertEqual(result, 29, 'One day old should be ~29 days');
  });

  test.it('returns 0 for expired trial', () => {
    const thirtyOneDaysAgo = Date.now() - (31 * 24 * 60 * 60 * 1000);
    const result = calculateTrialDaysRemaining(thirtyOneDaysAgo);
    test.assertEqual(result, 0, 'Expired should return 0');
  });

  test.it('never returns negative', () => {
    const hundredDaysAgo = Date.now() - (100 * 24 * 60 * 60 * 1000);
    const result = calculateTrialDaysRemaining(hundredDaysAgo);
    test.assertTrue(result >= 0, 'Should never be negative');
  });
});

