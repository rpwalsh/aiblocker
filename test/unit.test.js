test.describe('Unit: Error Handling', () => {
  function safeAnalyze(content) {
    try {
      if (!content || typeof content !== 'string') {
        throw new Error('Invalid content');
      }
      return { success: true, data: content.length };
    } catch (e) {
      return { success: false, error: e.message };
    }
  }

  test.it('handles valid input', () => {
    const result = safeAnalyze('test');
    test.assertTrue(result.success, 'Should succeed with valid input');
  });

  test.it('catches null error', () => {
    const result = safeAnalyze(null);
    test.assertFalse(result.success, 'Should fail with null');
    test.assertExists(result.error, 'Should have error message');
  });

  test.it('catches type error', () => {
    const result = safeAnalyze(123);
    test.assertFalse(result.success, 'Should fail with non-string');
  });

  test.it('continues after error', () => {
    const result1 = safeAnalyze(null);
    const result2 = safeAnalyze('test');
    test.assertTrue(result2.success, 'Should recover from error');
  });
});

