// Advanced ML-based Detection Module
// Uses heuristic analysis and pattern recognition for AI content detection

class AIContentDetector {
  constructor() {
    this.models = {
      text: new TextAnalyzer(),
      image: new ImageAnalyzer(),
      metadata: new MetadataAnalyzer()
    };
    this.cache = new Map();
    this.stats = {
      analyzed: 0,
      detected: 0,
      cached: 0
    };
  }

  async analyzeContent(content, type = 'text') {
    const hash = this.hashContent(content);
    
    // Check cache first
    if (this.cache.has(hash)) {
      this.stats.cached++;
      return this.cache.get(hash);
    }

    let result;
    if (type === 'text') {
      result = await this.models.text.analyze(content);
    } else if (type === 'image') {
      result = await this.models.image.analyze(content);
    } else {
      result = await this.models.metadata.analyze(content);
    }

    this.stats.analyzed++;
    if (result.isAiGenerated) {
      this.stats.detected++;
    }

    this.cache.set(hash, result);
    
    // Limit cache size
    if (this.cache.size > 1000) {
      const firstKey = this.cache.keys().next().value;
      this.cache.delete(firstKey);
    }

    return result;
  }

  hashContent(content) {
    let hash = 0;
    for (let i = 0; i < content.length; i++) {
      const char = content.charCodeAt(i);
      hash = ((hash << 5) - hash) + char;
      hash = hash & hash;
    }
    return hash.toString();
  }

  getStats() {
    return { ...this.stats };
  }
}

// Text Analysis using linguistic markers
class TextAnalyzer {
  constructor() {
    this.aiIndicators = {
      patterns: [
        /\b(furthermore|additionally|moreover|in conclusion)\b/gi,
        /\b(as an ai|as a language model|as an assistant)\b/gi,
        /\b(i apologize|i cannot|i don't|i'm unable)\b/gi,
        /\b(generated (by|using|with) (ai|ml|neural|gpt))\b/gi,
        /\b(machine learning generated|ai-generated|ai created)\b/gi
      ],
      punctuation: 0.7,
      entropy: 0.6,
      repetition: 0.5
    };
    this.vocabularyStats = new Map();
  }

  async analyze(text) {
    if (!text || text.length < 20) {
      return { isAiGenerated: false, confidence: 0, reasons: [] };
    }

    let score = 0;
    const reasons = [];

    // Check for AI pattern matches
    let patternMatches = 0;
    this.aiIndicators.patterns.forEach(pattern => {
      const matches = text.match(pattern);
      if (matches) {
        patternMatches += matches.length;
        reasons.push(`Found AI indicator: "${matches[0]}"`);
      }
    });
    score += Math.min(patternMatches * 0.15, 0.4);

    // Analyze sentence structure
    const sentences = text.match(/[^.!?]+[.!?]+/g) || [];
    if (sentences.length > 0) {
      const avgLength = text.split(/\s+/).length / sentences.length;
  // Unusual average sentence length (either very short or very long)
  if (avgLength > 25 || avgLength < 10) {
        score += 0.15;
        reasons.push("Unusual sentence structure patterns");
      }
    }

    // Check for repetitive vocabulary
    const words = text.toLowerCase().match(/\b\w+\b/g) || [];
    const uniqueWords = new Set(words);
    const repetitionRatio = 1 - (uniqueWords.size / words.length);
    if (repetitionRatio > 0.4) {
      score += 0.1;
      reasons.push("High vocabulary repetition detected");
    }

    // Check punctuation consistency
    const punctuation = text.match(/[.!?;:,]/g) || [];
    if (punctuation.length > 0 && punctuation.length / words.length > 0.15) {
      score += 0.05;
      reasons.push("Excessive punctuation usage");
    }

    return {
      isAiGenerated: score > 0.5,
      confidence: Math.min(score, 1),
      reasons,
      score
    };
  }
}

// Image metadata and visual analysis
class ImageAnalyzer {
  constructor() {
    this.aiImagePatterns = [
      'generated by ai',
      'ai image',
      'ai generated',
      'midjourney',
      'dall-e',
      'stable diffusion',
      'diffusion model'
    ];
  }

  async analyze(imageData) {
    let score = 0;
    const reasons = [];

    const { alt, title, src, dataAttrs } = imageData;
    const metadata = `${alt} ${title} ${src}`.toLowerCase();

    // Check filename patterns
    if (/ai[_-]?gen|generated|synthetic|artwork/.test(src)) {
      score += 0.2;
      reasons.push("AI-generated filename pattern detected");
    }

    // Check metadata
    this.aiImagePatterns.forEach(pattern => {
      if (metadata.includes(pattern)) {
        score += 0.25;
        reasons.push(`Found AI indicator in metadata: "${pattern}"`);
      }
    });

    // Check for suspicious dimensions (common in AI generators)
    if (imageData.width && imageData.height) {
      const ratio = imageData.width / imageData.height;
      if ((ratio > 0.666 && ratio < 0.667) || (ratio > 1.499 && ratio < 1.501)) {
        score += 0.1;
        reasons.push("Suspicious image dimensions (common in AI generation)");
      }
    }

    return {
      isAiGenerated: score > 0.5,
      confidence: Math.min(score, 1),
      reasons,
      score
    };
  }
}

// Metadata analysis for pages and content
class MetadataAnalyzer {
  async analyze(metadata) {
    let score = 0;
    const reasons = [];

    // Check page metadata
    if (metadata.description && metadata.description.includes('generated')) {
      score += 0.2;
      reasons.push("AI generation mentioned in page description");
    }

    if (metadata.keywords) {
      const keywords = metadata.keywords.toLowerCase();
      if (keywords.includes('ai') && keywords.includes('generated')) {
        score += 0.3;
        reasons.push("AI and generated keywords found together");
      }
    }

    return {
      isAiGenerated: score > 0.5,
      confidence: Math.min(score, 1),
      reasons,
      score
    };
  }
}

