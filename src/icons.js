/**
 * Convert base64 to PNG blob
 * @param {string} base64String - Base64 encoded PNG
 * @returns {Blob} PNG image blob
 */
function base64ToBlob(base64String) {
  // Remove newlines and extra whitespace from base64
  const cleaned = base64String.replace(/\s/g, '');
  
  const binaryString = atob(cleaned);
  const bytes = new Uint8Array(binaryString.length);
  
  for (let i = 0; i < binaryString.length; i++) {
    bytes[i] = binaryString.charCodeAt(i);
  }
  
  return new Blob([bytes], { type: 'image/png' });
}

/**
 * Get icon as data URL for manifest.json
 * @param {string} size - Icon size ('16', '48', or '128')
 * @returns {string} Data URL
 */
function getIconDataUrl(size) {
  if (size === '16') return `data:image/png;base64,${ICONS.icon16Base64.replace(/\s/g, '')}`;
  if (size === '48') return `data:image/png;base64,${ICONS.icon48Base64.replace(/\s/g, '')}`;
  if (size === '128') return `data:image/png;base64,${ICONS.icon128Base64.replace(/\s/g, '')}`;
  throw new Error(`Unknown icon size: ${size}`);
}

/**
 * Generate PNG files for Chrome Web Store
 * For use in build process or manual export
 */
async function generateIconFiles() {
  const files = {};
  
  files['icon-16.png'] = base64ToBlob(ICONS.icon16Base64);
  files['icon-48.png'] = base64ToBlob(ICONS.icon48Base64);
  files['icon-128.png'] = base64ToBlob(ICONS.icon128Base64);
  
  return files;
}

